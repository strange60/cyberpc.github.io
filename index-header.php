            </ul>
            <div class="st">Мышь</div>
            <p>Мыши обычно классифицируются по методу подключения как проводные и беспроводные. Также их можно классифицировать на основании датчика оптического или лазерного типа.</p>
            <p>Проводная мышь обычно подключается через порт USB, а беспроводная мышь используется Bluetooth и Wi-Fi на 2,4 ГГц. Для игр рекомендуется использовать проводную мышь во избежание проблем с потерей сигнала и разрядкой аккумулятора.</p>
            <p>Параметр DPI (точки на дюйм) — еще один критерий, который надо учитывать при покупке мыши. DPI определяет количество шагов — «точек» — которые мышь регистрирует при смещении на дюйм. Чем больше значение DPI, тем более чувствительна мышь, и тем быстрее ее указатель может перемещаться по экрану. Мышь с высоким показателем DPI сама по себе не гарантирует точность. Для полноценного ее использования вам потребуется экран высокого разрешения. Наиболее чувствительные мыши из представленных на рынке имеют показатель DPI, равный 8200. Стандартные мыши имеют показатель, равный примерно 1200.</p>
            <div class="st">Мониторы</div>
            <p>По форме мониторы можно разделить на плоские или изогнутые. При покупке монитора обратите внимание на тип панели, яркость, контрастность и частоту обновления. В настоящее время самыми популярными считаются панели IPS. Кроме учета личных потребностей, при выборе монитора вам следует учесть характеристики своей графической карты. Технологичные игровые мониторы могут поддерживать NVIDIA G-Sync или AMD Freesync — технологии, помогающие уменьшить разрыв изображения на экране. Как правило, мониторы получают выходные данные для отображения через интерфейсы D-Sub/DVI/HDMI.</p>
