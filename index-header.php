            <div class="st">Клавиатура</div>
            <p>Клавиатура используется преимущественно для ввода текста. Клавиатуры используют интерфейсы USB и PS/2. Чаще всего используются USB-клавиатуры, а PS/2 применяется только в отдельных современных или устаревших моделях.</p>
            <p>Механизм и способ работы клавиатуры определяют ее отклик на нажатие при печати. Есть два основных типа клавиатур: мембранные и механические.</p>
            <ul>
                <li>Механическая клавиатура обладает независимыми физическими переключателями. Различные переключатели обладают разными откликами на нажатие. Основные типы таких клавиатур носят кодовые названия Brown, Blue, Black и White.</li>
