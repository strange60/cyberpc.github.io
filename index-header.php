                <li>Серия Ryzen Самые распространенные процессоры AMD. В этой линейке собраны продукты для пользователей любого уровня&nbsp;— от новичков до профессионалов.</li>
                <li>Ryzen 7: Восьмиядерный процессор. Подходит для профессиональных геймеров и продвинутых пользователей, работающих со множеством задач и большим количеством данных.</li>
                <li>Ryzen 5: Четырех- и шестиядерные процессоры. Хотя и уступают Ryzen&nbsp;7 в производительности, они не боятся многозадачности. К примеру, вы можете включить потоковую передачу данных и одновременно наслаждаться любимой игрой.</li>
                <li>Ryzen 3: Четырехъядерный процессор со сравнительно невысокой производительностью. Подходит для казуальных игр, офисной работы и развлечений.</li>
                <li>AMD Athlon и AMD серии&nbsp;Двухъядерные процессоры со встроенной видеокартой, предназначенные для начинающих пользователей, которым не нужна отдельная видеокарта для просмотра видео, работы с текстами и сёрфинга в Сети.</li>
