            <p>Процессор или ЦП или CPU — «мозг» компьютера. Его основное предназначение — разбирать указания компьютера и обрабатывать данные компьютерных программ.</p>
            <p>На рынке представлены два основных производителя ЦП: Intel и AMD. У обоих есть свои преимущества. Важно отметить, что на разных материнских платах есть разные соответствующие разъемы под процессор, сокеты. Например, платформа Intel X299 работает только с процессорами Intel, использующими разъем LGA 2066. Как правило, у современных ЦП больше номера моделей. Помимо цены, разница в производительности отражается количеством ядер, частотой синхроимпульсов, возможностью разгона и размером кэша.</p>
            <p>Ниже приводится перечень основных разъемов и процессоров, представленных на современном рынке.</p>
            <div class="st">Intel</div>
            <h3>Основные серии процессоров Intel:</h3>
            <ul>
                <li>Celeron: одна из самых недорогих моделей Intel с начальным уровнем производительности. Подходит для бюджетных ПК, в основном используемых для просмотра видео, работы с текстом и просмотра сетевых ресурсов.</li>
                <li>Pentium: отличается лучшей по сравнению с Celeron производительностью при худших показателях внутренних мощностей обработки графики и меньших размерах кэша по сравнению с серией Core i.</li>
                <li>Core i: на данный момент основная серия процессоров на рынке.</li>
                <li>Core i3: не поддерживает Turbo Boost, подходит для игр с низкими требованиями.</li>
                <li>Core i5: поддерживает Turbo Boost, подходит для игр со средними и высокими требованиями.</li>
                <li>Core i7: поддерживает Turbo Boost, цена зависит от количества ядер. Подходит для заядлых геймеров, которым нужна качественная графика, либо пользователям, которые часто запускают программы редактирования видео или изображений.</li>
                <li>Xeon: обычно используется на рабочих станциях и серверах и не обладает встроенными мощностями обработки графики..</li>
            </ul>
